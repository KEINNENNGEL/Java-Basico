public class Premio {
		private String descripcion;
		private int punjate;
		
		public Premio(String descripcion,int puntaje){
			this.descripcion=descripcion;
			this.punjate=puntaje;
		}
		public String getDescripcion(){
			return descripcion;
		}
		public int getPuntaje(){
			return punjate;
		}
		
	

}
