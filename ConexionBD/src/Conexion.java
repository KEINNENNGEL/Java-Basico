    public void conectar(){
        server=jTextField1.getText();
        db=jTextField2.getText();
        usuario=jTextField3.getText();
        contrasenia=jTextField4.getText();
        url="jdbc:mysql://"+server+"/"+db;
        
        try{
           Class.forName("com.mysql.jdbc.Driver");
           conn=(Connection) DriverManager.getConnection(url, usuario, contrasenia);
           //conn=DriverManager.getConnection(url,usuario,contrasenia);
           if(conn!=null){
               jButton4.setVisible(true);
               
               //System.out.println("Conexion de bd "+url+" ok");
               JOptionPane.showMessageDialog(null,"Conexion de bd "+url+" ok");
               conn.close();
           }
        }catch(SQLException ex){
            System.out.println("Error en la conexion");
        }
        catch(ClassNotFoundException es){
            System.out.println(es);
        }
    }
