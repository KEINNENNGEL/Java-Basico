    public void escribeTxt(String textoNuevo){
        File archivo;
        archivo=new File("configConex.txt");
        try{
            FileWriter scribe=new FileWriter(archivo);
            BufferedWriter buffere=new BufferedWriter(scribe);
            PrintWriter imprime=new PrintWriter(buffere);
            imprime.write(textoNuevo);
            imprime.close();
            buffere.close();
            if(archivo.exists()){
                JOptionPane.showMessageDialog(rootPane,"Archivo Creado Correctamente");
            }else{
                JOptionPane.showMessageDialog(rootPane, "No se puede crear el archivo");
            }
        }catch (IOException x){
            JOptionPane.showMessageDialog(null,x.getMessage());
        }
        }
    public void leer() {

        File archivo = null;
        FileReader fr = null;
        BufferedReader br = null;

        String linea = null;

        try {
            //Cargamos el archivo de la ruta relativa
            archivo = new File("configConex.txt");
            //Cargamos el objeto FileReader
            fr = new FileReader(archivo);
            //Creamos un buffer de lectura
            br = new BufferedReader(fr);

            String[] datos = null;
            //String identificador=datos[0];
            String matricula="localhost";
            //Leemos hasta que se termine el archivo
            while ((linea = br.readLine()) != null) {
                    //Utilizamos el separador para los datos
                datos = linea.split(":");
                if(datos[0].equals(matricula)){
                //Presentamos los datos
                jTextField1.setText(datos[0]); 
                jTextField2.setText(datos[1]); 
                jTextField3.setText(datos[2]); 
                jTextField4.setText(datos[3]); 
                }
            } //Capturamos las posibles excepciones
        } catch (Exception e) {
            e.printStackTrace();
        } finally {
            try {
                if (fr != null) {
                    fr.close();
                }
            } catch (Exception e2) {
                e2.printStackTrace();
            }
        }

    }////////////////////////////////////////////////
    
